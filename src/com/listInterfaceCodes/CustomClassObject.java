package com.listInterfaceCodes;

public class CustomClassObject {

	int bookId;
	String bookName, publisher, author;
	int price;

	public CustomClassObject(int bookId, String bookName, String publisher, String author, int price) {
		super();
		this.bookId = bookId;
		this.bookName = bookName;
		this.publisher = publisher;
		this.author = author;
		this.price = price;
	}

}
